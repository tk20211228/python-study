for i in range(1,101):  # 1から100までの範囲でループを行います。
    if i % 3 == 0 and i % 5 == 0:  # iが3と5の両方で割り切れる場合（つまり、15で割り切れる場合）
        print('{}: FizzBuzz'.format(i))  # 'FizzBuzz'を出力します。実行結果は'15: FizzBuzz', '30: FizzBuzz', ... , '90: FizzBuzz'が順に表示されます。
    elif i % 3 == 0:  # iが3で割り切れる場合（ただし、上記の15で割り切れる場合は除く）
        print('{}: Fizz'.format(i))  # 'Fizz'を出力します。実行結果は'3: Fizz', '6: Fizz', ... , '99: Fizz'が順に表示されます。
    elif i % 5 == 0:  # iが5で割り切れる場合（ただし、上記の15で割り切れる場合は除く）
        print('{}: Buzz'.format(i))  # 'Buzz'を出力します。実行結果は'5: Buzz', '10: Buzz', ... , '100: Buzz'が順に表示されます。
    else:  # 上記のいずれの条件も満たさない場合
        print(i)  # iの値をそのまま出力します。実行結果は1, 2, 4, 7, 8, 11, 13, 14, 16, 17, 19, ... , 98が順に表示されます。

# このコードは、1から100までの数値に対して、以下のルールに基づいて出力を行います。
# ・数値が3と5の両方で割り切れる場合は、'FizzBuzz'を出力します。
# ・数値が3で割り切れる場合は、'Fizz'を出力します。
# ・数値が5で割り切れる場合は、'Buzz'を出力します。
# ・上記のいずれの条件も満たさない場合は、数値をそのまま出力します。
